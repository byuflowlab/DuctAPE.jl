# Generate smoother geometry
function circlecoords(npts, r=0.16)
    t = range(pi, pi / 2.0; length=npts)
    return r .+ r * cos.(t), r * sin.(t)
end

N = 30
avg_ds = pi / 2.0 / N
xcirc, ycirc = circlecoords(N)
xcylendidx = findlast(x -> x == 0.16, r)
xcyl = collect(range(0.16, x[xcylendidx]; step=avg_ds))
ycyl = 0.16 * ones(length(xcyl))
xwedge = range(
    xcyl[end], maximum(x); length=(round(Int, (maximum(x) - xcyl[end]) / avg_ds))
)
ywedge = 0.16 .- 0.16 / (xwedge[end] - xwedge[1]) * (xwedge .- xwedge[1])

xsmooth = [xcirc; xcyl[2:(end - 1)]; xwedge]
rsmooth = [ycirc; ycyl[2:(end - 1)]; ywedge]

